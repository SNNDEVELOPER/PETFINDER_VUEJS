<template>
  <div id="app">
    <b-container>
      <transition
        name="fade"
        mode="out-in"
      >
        <router-view></router-view>
      </transition>
    </b-container>
  </div>
</template>

<script>

export default {
  name: 'App',
  watch: {
  '$route' (to, from) {
    const toDepth = to.path.split('/').length
    const fromDepth = from.path.split('/').length
    this.transitionName = toDepth < fromDepth ? 'slide-right' : 'slide-left'
  }
}
}
</script>

